/* 
---------------------------------------------------------------------
FUNÇÃO

Uma função JS é um bloco de código projetado para executar uma tarefa
especifica.

É como uma pequena "fábrica" onde você faz uma entrada e ele te dá 
uma saida.

Pode ser encarado como "mini-programas" projetados para fazer uma 
tarefa que vai contribuir para todo código.

Uma função JS é executada quando "algo" a invoca.
*/
